package JavaBasic.Basic;

//Write a Java program to find the product of two floating-point numbers.

public class ProductOfFloats {
    public static void main(String[] args) {
        float a = 1.5f;
        float b = 2.5f;

        float product = a * b;
        System.out.println(" The Product of floats a = "+a+" and b = "+b+"is = "+product);
    }
}
