package JavaBasic.Basic;

//Write a Java program to calculate the sum of two integers.

public class SumOfIntegers {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        int sum = a + b;
        System.out.println(" the sum of a = "+ a+ " and b = "+ b+ " is = "+sum);
    }
}
