package JavaBasic.Basic;

//Write a Java program to find the square and cube of a number.

public class SquareAndCube {
    public static void main(String[] args) {
        int num = 10;
        int square = num * num;
        int cube = square * num;

        System.out.println(" The square of num "+num+" is = "+square);
        System.out.println(" The cube of num "+num+ " is = "+cube);
    }
}
