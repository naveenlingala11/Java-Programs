package JavaBasic.Loops;

//Write a Java program to print numbers from 1 to 100 using a for loop.
public class PrintNumbersFor {
    public static void main(String[] args) {
        System.out.println("Numbers from 1 to 100 using for loop:");
        for ( int i = 1; i <= 100; i++){
            System.out.println(i);
        }
    }
}
